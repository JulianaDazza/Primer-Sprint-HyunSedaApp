    /**
     *@brief función que llena el comboBox de manera automática
     */
    private void fillCombo(){
        int max = productService.listProducts().size();//Obtener el tamaño de la lista de productos
        for(int i = 0; i < max;i++)//Hacer el recorrido
        {
            cbxProducts.addItem(productService.listProducts().get(i).getName());//LLenado del comboBox
        }
    }

