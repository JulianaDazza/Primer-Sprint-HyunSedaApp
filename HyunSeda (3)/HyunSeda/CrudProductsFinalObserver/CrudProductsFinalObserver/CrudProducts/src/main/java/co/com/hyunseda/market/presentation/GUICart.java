    private void fillTable(List<Product> prmProductList) {
    initializeTable();
    
    
    DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();

    Object rowData[] = new Object[3];//No columnas
        
        for (int i = 0; i < prmProductList.size(); i++) {
            rowData[0] = prmProductList.get(i).getProductId();
            rowData[1] = prmProductList.get(i).getName();
            rowData[2] = prmProductList.get(i).getPrice();

            model.addRow(rowData);
        }
    }
